package variable;
import java.lang.*;	


public class Test06 {
	public static void main(String[] args) {
		
		//문자열
		// - String		가변형
		//	참조형
		
		String name = "황현성";
		
		System.out.println(name);
		
		name = "김민준";
		
		System.out.println(name);
		
		//문자열은 연산 불가. 단, 더하기는 가능
		
		System.out.println(name + 100);
		System.out.println(name + (100 + 200));
		System.out.println(100 + 200 + name);
		
		
		
		
		
		
	}

}












